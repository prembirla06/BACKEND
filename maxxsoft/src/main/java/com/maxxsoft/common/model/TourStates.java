package com.maxxsoft.common.model;

/**
 * @author Mahinga Singh
 * @email ms@algoson.com
 */
public enum TourStates {
	PENDING(101, "Tour is Not started Yet"), STARTED(102, "Tour is on the way"), COMPLETED(103, "TOUR is completed");

	private final int code;
	private final String message;

	TourStates(int code, String message) {
		this.code = code;
		this.message = message;
	}

	public int getCode() {
		return code;
	}

	public String getMessage() {
		return message;
	}
}
